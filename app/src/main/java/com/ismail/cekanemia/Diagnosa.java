public class Diagnosa {

    private String gejalaKlinis;
    private double aplastik;
    private double db;
    private double pk;
    private double hemolitik;

    public String getGejalaKlinis() {
        return gejalaKlinis;
    }

    public void setGejalaKlinis(String gejalaKlinis) {
        this.gejalaKlinis = gejalaKlinis;
    }

    public double getAplastik() {
        return aplastik;
    }

    public void setAplastik(double aplastik) {
        this.aplastik = aplastik;
    }

    public double getDb() {
        return db;
    }

    public void setDb(double db) {
        this.db = db;
    }

    public double getPk() {
        return pk;
    }

    public void setPk(double pk) {
        this.pk = pk;
    }

    public double getHemolitik() {
        return hemolitik;
    }

    public void setHemolitik(double hemolitik) {
        this.hemolitik = hemolitik;
    }
}
